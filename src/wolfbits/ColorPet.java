
package wolfbits;

import java.io.File;


public class ColorPet {
   
    private String name;
    private File photo;

    public ColorPet(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public File getPhoto() {
        return photo;
    }

    public void setPhoto(File photo) {
        this.photo = photo;
    }

   
    
    
}
